app.get('/api/supervisor/agents/:agentId/reports', authenticate, requireRole(['supervisor']), async (req, res) => {
  try {
    const { agentId } = req.params;
    const { period = 'today' } = req.query;
    
    // VÃ©rifier que l'agent appartient au superviseur
    const agent = await Agent.findOne({ 
      agentId: agentId,
      supervisorId: req.user.username 
    });
    
    if (!agent) {
      return res.status(404).json({
        success: false,
        message: 'Ajan pa jwenn oswa ou pa gen aksÃ¨'
      });
    }
    
    // DÃ©finir la pÃ©riode
    let startDate = new Date();
    startDate.setHours(0, 0, 0, 0);
    
    if (period === 'week') {
      startDate.setDate(startDate.getDate() - 7);
    } else if (period === 'month') {
      startDate.setMonth(startDate.getMonth() - 1);
    } else if (period === 'year') {
      startDate.setFullYear(startDate.getFullYear() - 1);
    }
    
    // RÃ©cupÃ©rer les tickets
    const tickets = await Ticket.find({ 
      agentId: agentId,
      date: { $gte: startDate }
    });
    
    // RÃ©cupÃ©rer les gains
    const winners = await Winner.find({
      agentId: agentId,
      date: { $gte: startDate }
    });
    
    // Calculer les totaux
    const totalTickets = tickets.length;
    const totalBets = tickets.reduce((sum, ticket) => sum + ticket.total, 0);
    const totalWins = winners.reduce((sum, winner) => sum + winner.winningAmount, 0);
    const totalLoss = totalBets - totalWins;
    const balance = totalWins - totalLoss;
    const successRate = totalTickets > 0 ? (winners.length / totalTickets) * 100 : 0;
    
    res.json({
      success: true,
      totalTickets,
      totalBets,
      totalWins,
      totalLoss,
      balance,
      successRate
    });
    
  } catch (error) {
    console.error('Erreur rÃ©cupÃ©ration rapports agent:', error);
    res.status(500).json({
      success: false,
      message: 'ErÃ¨ rÃ©cupÃ©ration rapÃ² ajan'
    });
  }
});

